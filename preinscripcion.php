<h1>INSCRIPCIÓN A CARRERAS DE NIVEL TERCIARIO</h1>

<p>Mediante la web del Instituto, se llevará adelante una pre inscripción en la cual se otorgará un turno para entregar la documentación inherente al ingreso a la institución terciaria que solicitará la oficina de alumnado.</p>

<p>La inscripción al instituto es provisoria hasta que se presente la documentación de estudios secundarios completos, legalizada por autoridad competente. El aspirante tiene tiempo hasta el xx/xx/20xx para entregar la constancia de finalización del colegio secundario, donde se consigne NO adeudar materias o Certificados de finalización de estudios en trámite, de no ser así la solicitud de inscripción será denegada y dada de baja de los registros, no pudiendo el aspirante cursar materia alguna. La eventual aprobación de asignaturas por alumnos que no hayan cumplido con este requisito de admisión carecerá de validez y no será reconocida por la Escuela Superior Nº 49 Capitán General Justo José de Urquiza.</p>

<p>Los requisitos que deberá entregar el aspirante son los siguientes:</p>
<ul>
    <li>Documento de identidad: Fotocopia certificada por autoridad competente.</li>
    <li>Partida de nacimiento: correctamente legalizada.</li>
    <li>Título secundario: Fotocopia autenticada por Poder Judicial o Constancia (original) en donde se consigne que ha cursado y aprobado los estudios correspondientes al nivel de enseñanza media.</li>
    <li>2 fotos, 3cm x 3cm, tipo carnet.</li>
</ul>

